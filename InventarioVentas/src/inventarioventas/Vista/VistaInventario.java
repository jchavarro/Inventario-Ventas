    private void btnprestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprestarActionPerformed
        if (nombreprestar.getText().equals("")|| suproducto.getText().equals("")|| miproducto.getText().equals("")
                ||tfechaprestar.getCalendar() == null) {
            JOptionPane.showMessageDialog(this,"Llene todos los campos");
        }else{
            if(conexion.existeproducto(suproducto.getText())&&conexion.existeproducto(miproducto.getText())){
                int año = tfechaprestar.getCalendar().get(Calendar.YEAR);
                int mes = tfechaprestar.getCalendar().get(Calendar.MONTH);
                int dia = tfechaprestar.getCalendar().get(Calendar.DAY_OF_MONTH);
                String fecha = "" + año + "-" + mes + "-" + dia;
                conexion.prestar(nombreprestar.getText(),suproducto.getText(),miproducto.getText(),fecha);
                limpiar();
                modeloPrestar = conexion.obtenerTablaprestar(modeloPrestar);
                tfechaprestar.setCalendar(null);
            }else{JOptionPane.showMessageDialog(this, "no existe ese preducto!");}
            
        }
    }//GEN-LAST:event_btnprestarActionPerformed

