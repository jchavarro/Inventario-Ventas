    private void btnvenderfinalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnvenderfinalActionPerformed
        
        if (Tnombreproducto.getText().equals("") || ((int)spinerventa.getValue()) == 0 || Tnombrecliente.getText().equals("")
                ||tfecha.getCalendar() == null) {
            JOptionPane.showMessageDialog(this,"Llene todos los campos");
        }else{
            
            int año = tfecha.getCalendar().get(Calendar.YEAR);
            int mes = tfecha.getCalendar().get(Calendar.MONTH);
            int dia = tfecha.getCalendar().get(Calendar.DAY_OF_MONTH);
            String fecha = "" + año + "-" + mes + "-" + dia;
            conexion.vender(Tnombreproducto.getText(),(int)spinerventa.getValue(),Tnombrecliente.getText(),fecha);
            conexion.obtenerTablacaja(modeloCaja);
            JOptionPane.showMessageDialog(this,"Vendido!");
            Tnombreproducto.setText("");
            Tnombrecliente.setText("");
            spinerventa.setValue(0);
            tfecha.setCalendar(null);
        }
    }//GEN-LAST:event_btnvenderfinalActionPerformed

